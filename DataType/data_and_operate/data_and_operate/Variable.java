package data_and_operate;

public class Variable {

	public static void main(String[] args) {
		
		//변할 수 있는 문자 : 변수
		int a = 1; // integer(자연수 + -1,-2,-3)
		System.out.println(a);
		
		double b = 1.1; //double real Number ... -2.0, -1.0, 0, 1.0,1.5912759 ,2.0
		System.out.println(b);
		
		String c = "Hello World!!"; //문자열 
		System.out.println(c);
		
		//왜 데이터타입을 변수에 정의를 해주어야하는가
		//꺼낼때마다 변수를 확인하지 않아도 된다.
		
		

	}

}
